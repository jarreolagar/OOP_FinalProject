    public void cargarInventario() throws SQLException{
        Statement stm=new ConexionBD().getConnection().createStatement();
        ResultSet rst=stm.executeQuery("SELECT * FROM Inventario");
        limpiarBufferInventario();
        while(rst.next()){
            dtm.addRow(new Object[]{
                rst.getInt(1),
                rst.getString(2),
                rst.getInt(3),
                rst.getFloat(4),
                rst.getInt(5),
                rst.getString(6),
                rst.getString(7)
            });
        }
    }
    
    private void limpiarBufferInventario() {
        int filas=dtm.getRowCount();
        for(int i=0;i<filas;i++){
            dtm.removeRow(0);
        }
    }
